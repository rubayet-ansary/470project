    private void doneButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_doneButtonActionPerformed
        // TODO add your handling code here:
        if(emailField.getText()==null || course1Field.getText()==null || sec1Field.getText()==null || course2Field.getText()==null || sec2Field.getText()==null || course3Field.getText()==null || sec3Field.getText()==null || course4Field.getText()==null || sec4Field.getText()==null || course5Field.getText()==null || sec5Field.getText()==null)
        {
            noteLabel.setText("Invalid Data!");
        }
        else
        {
            if(passField.getText().length()!=0)
            {
                if(passField.getText().length()<4)
                {
                    noteLabel.setText("Password length must be at least 4 digits.");
                }
                else
                {
                    try 
                    {
                        String str;
                        if(new DBConnect().stdORfac(user))
                        {
                            str="std";
                        }
                        else
                        {
                            str="fac";
                        }
                        new DBConnect().update(user, str, passField.getText(), emailField.getText(), course1Field.getText(), sec1Field.getText(), course2Field.getText(), sec2Field.getText(), course3Field.getText(), sec3Field.getText(), course4Field.getText(), sec4Field.getText(), course5Field.getText(), sec5Field.getText(), bloodField.getText());
                        new DBConnect().setPass(user, passField.getText());
                        noteLabel.setText("Note: Update Successful!");
                    } catch (SQLException ex) {
                        Logger.getLogger(editProfile.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            }
            else
            {
                try 
                    {
                        String str;
                        if(new DBConnect().stdORfac(user))
                        {
                            str="std";
                        }
                        else
                        {
                            str="fac";
                        }
                        new DBConnect().update(user, str, passField.getText(), emailField.getText(), course1Field.getText(), sec1Field.getText(), course2Field.getText(), sec2Field.getText(), course3Field.getText(), sec3Field.getText(), course4Field.getText(), sec4Field.getText(), course5Field.getText(), sec5Field.getText(), bloodField.getText());
                       
                        noteLabel.setText("Note: Update Successful!");
                    } catch (SQLException ex) {
                        Logger.getLogger(editProfile.class.getName()).log(Level.SEVERE, null, ex);
                    }
            }
        }
    }//GEN-LAST:event_doneButtonActionPerformed

